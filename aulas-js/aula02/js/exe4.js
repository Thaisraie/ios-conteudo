let num1 = Number(prompt("Por favor, insira um número: " ));
let num2 = Number(prompt("Por favor, insira outro número: " ));

let soma = num1 + num2;
let subtracao = num1 - num2;
let multiplicacao = num1 * num2;
let divisao = num1 / num2;

alert(`Olá! A soma do número: ${num1} e ${num2} é: ${soma}, a subtração é: ${subtracao}, a multiplicação é: ${multiplicacao} e a divisão é: ${divisao}`);